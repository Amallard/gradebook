		<h2>Why?</h2>
		<p>CougarTrack is garbage. In the 'Grades' section of CougarTrack, you are only provided with a list 
				of all of your assignments, the total points they are worth, and the grade you received. If you want 
				to know your overall grade in the class at any given time, you need to add up all of the possible points, 
				add up all of the points you earned, and figure out your grade that way. This web application 
				does all of that for you. It keeps a running total of your grade in the course. After setting everything up, 
				all you need to do is log on and input the scores you receive on assignments. You are also able to easily 'play around' with
				the assignment grades, as in "If I get a 70% on the final, what would my final grade be? Well what if I get an 80%?"
		</p>
		
